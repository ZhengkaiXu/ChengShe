#include <iostream>
using namespace std;

int main() {
    cout << "3>2 的逻辑值是" << (3 > 2) << ", 1为真。 " << endl;
    cout << "3>=2 的逻辑值是" << (3 >= 2) << ", 1为真。 " << endl;
    cout << "3==2 的逻辑值是" << (3 == 2) << ", 0为假。 " << endl;
    cout << "3<2 的逻辑值是" << (3 < 2) << ", 0为假。 " << endl;
    cout << "3<=2 的逻辑值是" << (3 <= 2) << ", 0为假。 " << endl;
    cout << "3!=2 的逻辑值是" << (3 != 2) <<  ", 1为真。 " << endl;
    return 0;
}